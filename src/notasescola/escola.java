package notasescola;

/**
 *
 * @author User1
 */
public class escola {
    private String materia;
    private String codigo;
    private double N1;
    private double N2;
    private double N3;
    private double N4;

    public escola() {
    }

    public escola(String materia, String codigo, double N1, double N2, double N3, double N4) {
        this.materia = materia;
        this.codigo = codigo;
        this.N1 = N1;
        this.N2 = N2;
        this.N3 = N3;
        this.N4 = N4;
    }

    
    
    
    
    /**
     * @return the materia
     */
    public String getMateria() {
        return materia;
    }

    /**
     * @param materia the materia to set
     */
    public void setMateria(String materia) {
        this.materia = materia;
    }

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the N1
     */
    public double getN1() {
        return N1;
    }

    /**
     * @param N1 the N1 to set
     */
    public void setN1(double N1) {
        this.N1 = N1;
    }

    /**
     * @return the N2
     */
    public double getN2() {
        return N2;
    }

    /**
     * @param N2 the N2 to set
     */
    public void setN2(double N2) {
        this.N2 = N2;
    }

    /**
     * @return the N3
     */
    public double getN3() {
        return N3;
    }

    /**
     * @param N3 the N3 to set
     */
    public void setN3(double N3) {
        this.N3 = N3;
    }

    /**
     * @return the N4
     */
    public double getN4() {
        return N4;
    }

    /**
     * @param N4 the N4 to set
     */
    public void setN4(double N4) {
        this.N4 = N4;
    }
    
    
    
    
    
    
}
